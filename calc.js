let x, y;
x = parseFloat(prompt("Enter first number: "));
y = parseFloat(prompt("Enter second number: "));
addition = x + y;
substraction = x - y;
multiplication = x * y;
division = x / y;

//addition operation
alert(addition);
console.log("The sum of ", x, "and", y, "is", addition);
//subtraction operation
alert(substraction);
console.log("The substraction of ", y, "from", x, "is", substraction);
//multiplication operation
alert(multiplication);
console.log("The multiple of ", x, "and", y, "is", multiplication);
//division operation
alert(division);
console.log("The division of ", x, "and", y, "is", division);
